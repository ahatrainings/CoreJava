package org.aha;

public class StringDemo {

	
	public static void main(String args[]) {
		
		//Strings are immutable
		
		//Cannot modify the strings
		
		String str = "Jana";  //2301258
		
		System.out.println(str);
		System.out.println(str.hashCode());
		
		str = "Anu"; //65992
		
		
		System.out.println(str);
		System.out.println(str.hashCode());
		
		//StringBufferDemo
		
		
		
		
		
	}
}

